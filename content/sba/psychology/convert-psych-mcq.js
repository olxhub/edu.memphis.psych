#!/usr/bin/env node
// src/scripts/convert-psych-mcq.js
//
// Converts psychology multiple-choice questions from JSON to OLX format.
//
// Usage:
//   npx tsx content/sba/psychology/convert-psych-mcq.js content/sba/psychology/operant-questions.json
//   npx tsx content/sba/psychology/convert-psych-mcq.js content/sba/psychology/function-questions.json
//
// Output:
//   - Creates an .auto.olx file with CapaProblems wrapped in a Vertical.
//   - Creates an .auto.idlistpeg file with problem IDs for MasteryBank src=.
// Answer options and ID prefix are derived from the data and filename.

import fs from 'fs';
import path from 'path';

function escapeXml(str) {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

function slugify(str) {
  return str.toLowerCase().replace(/[/\s]+/g, '_');
}

function generateProblemId(index, key, prefix) {
  return `${prefix}_${String(index + 1).padStart(3, '0')}_${slugify(key)}`;
}

function titleCase(str) {
  return str.charAt(0).toUpperCase() + str.slice(1);
}

function questionToOlx(question, index, answerOptions, prefix) {
  const { stem, key, explanation, title } = question;
  const problemId = generateProblemId(index, key, prefix);
  const graderId = `${problemId}_grader`;
  const inputId = `${problemId}_input`;

  const choicesXml = answerOptions.map(option => {
    const optionId = `${problemId}_${slugify(option)}`;
    const isKey = option === key;
    const tag = isKey ? 'Key' : 'Distractor';
    const label = titleCase(option);
    return `        <${tag} id="${optionId}">${escapeXml(label)}</${tag}>`;
  }).join('\n');

  return `  <CapaProblem id="${problemId}" title="${escapeXml(title)}">
    <KeyGrader id="${graderId}">
      <p>${escapeXml(stem)}</p>
      <ChoiceInput id="${inputId}">
${choicesXml}
      </ChoiceInput>
      <Explanation showWhen="answered">${escapeXml(explanation)}</Explanation>
    </KeyGrader>
  </CapaProblem>`;
}

function convertJsonToOlx(jsonPath) {
  const jsonContent = fs.readFileSync(jsonPath, 'utf-8');
  const questions = JSON.parse(jsonContent);

  const basename = path.basename(jsonPath, '.json');
  const dirname = path.dirname(jsonPath);
  const outputPath = path.join(dirname, `${basename}.auto.olx`);
  const idlistPath = path.join(dirname, `${basename}.auto.idlistpeg`);

  // Derive prefix and title from filename: "operant-questions" → "operant", "function-questions" → "function"
  const prefix = basename.replace(/-questions$/, '').replace(/-/g, '_');
  const displayTitle = titleCase(prefix.replace(/_/g, ' ')) + ' Practice';
  const verticalId = `${prefix}_problems`;

  // Derive answer options from the data
  const answerOptions = [...new Set(questions.map(q => q.key))].sort();

  const problemsXml = questions.map((q, i) => questionToOlx(q, i, answerOptions, prefix)).join('\n\n');

  // Generate ID list for easy copy-paste into MasteryBank
  const idList = questions.map((q, i) => generateProblemId(i, q.key, prefix)).join('\n');

  const olxContent = `<!--
---
description: "Question bank for ${prefix.replace(/_/g, ' ')} mastery assessment in psychology course. Not an independent activity -- included to allow direct access to the content. Note that this file is automatically generated, so edits will be overwritten when the system is rebuilt."
category: psychology
lang: en-Latn-US
generated:
  method: build
  source_file: ${path.basename(jsonPath)}
---
-->
<!-- Generated from ${path.basename(jsonPath)} -->
<!-- ${questions.length} questions, ${answerOptions.length} answer categories -->
<!--
Problem IDs for MasteryBank:
${idList}
-->

<Vertical id="${verticalId}" title="${displayTitle}" launchable="true">
${problemsXml}
</Vertical>
`;

  fs.writeFileSync(outputPath, olxContent);
  fs.writeFileSync(idlistPath, idList + '\n');
  console.log(`✅ Generated ${outputPath}`);
  console.log(`✅ Generated ${idlistPath}`);
  console.log(`   ${questions.length} problems created`);
  console.log(`   Answer options: ${answerOptions.join(', ')}`);

  // Print distribution
  const distribution = {};
  for (const q of questions) {
    distribution[q.key] = (distribution[q.key] || 0) + 1;
  }
  console.log('   Distribution:');
  for (const [key, count] of Object.entries(distribution).sort()) {
    console.log(`     ${key}: ${count}`);
  }
}

// Main
const args = process.argv.slice(2);
if (args.length === 0) {
  console.log('Usage: npx tsx content/sba/psychology/convert-psych-mcq.js <json-file>');
  process.exit(1);
}

const jsonPath = args[0];
if (!fs.existsSync(jsonPath)) {
  console.error(`❌ File not found: ${jsonPath}`);
  process.exit(1);
}

convertJsonToOlx(jsonPath);
